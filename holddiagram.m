%This script is used to create a diagram to illistrate the plot
%representing holdback

clear;
close all;
figure;
hold on;
x=linspace(0,3,100);
y=cumtrapz(x,genncstr(x,3,1));
newx=x(1:find(x==1));
A1=fill([newx flip(newx)], [y(1:find(x==1)) zeros(1,find(x==1))],'cyan',...
    'displayname','Hold-back');
x2 = x(find(x==1):end);
fill([x2 flip(x2)], [ones(1,length(x2)) flip(y(find(x==1):end))],'cyan');

p=plot(x,y,'LineWidth',2.5,'displayname','Reactor F($\theta$)');
title('Holdback (H)','FontSize',20);
xlabel('Dimensionless time($\theta = \frac{time}{\bar{t}_{avg}})$','Interpreter','Latex');
ylabel('$F(\theta )$','Interpreter','Latex');
xline(1,'LineWidth',2.5);
yline(1,'LineWidth',2.5);
ylim([0 1.15]);
annotation('textbox',...
    [0.274285714285714 0.152380952380952 0.0608333333333334 0.0952380952380952],...
    'VerticalAlignment','middle',...
    'String','A',...
    'FontWeight','bold',...
    'FontSize',18,...
    'FitBoxToText','off');
annotation('textbox',...
    [0.421904761904762 0.677777777777779 0.0608333333333333 0.0952380952380952],...
    'VerticalAlignment','middle',...
    'String','A',...
    'FontWeight','bold',...
    'FontSize',18,...
    'FitBoxToText','off');
annotation('textbox',...
    [0.192142857142857 0.561111111111111 0.0977380952380956 0.103968253968254],...
    'VerticalAlignment','middle',...
    'String','1-A',...
    'FontWeight','bold',...
    'FontSize',18,...
    'FitBoxToText','off');
legend([A1 p],'Interpreter','Latex')
saveas(gcf,'pic\hold','epsc')
saveas(gcf,'pic\hold','png')
